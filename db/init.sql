CREATE DATABASE TICKETSYSTEM CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE TICKETSYSTEM;

CREATE TABLE ORDERS (
    ORDER_NUMBER INT,
    PRODUCT_NAME VARCHAR(30),
    PRODUCT_QUANTITY INT,
    ADD_BEEF BOOLEAN,
    ADD_EGG BOOLEAN,
    ORDER_TIME TIME
) CHARACTER SET utf8mb4;

CREATE TABLE ORDER_LOG (
    ORDER_NUMBER INT,
    PRODUCT_NAME VARCHAR(30),
    PRODUCT_QUANTITY INT,
    ADD_BEEF BOOLEAN,
    ADD_EGG BOOLEAN,
    ORDER_TIME TIME
) CHARACTER SET utf8mb4;

ALTER USER 'root'@'%' IDENTIFIED BY 'ROOT';
FLUSH PRIVILEGES;